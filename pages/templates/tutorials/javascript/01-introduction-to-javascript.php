  <div>
    <ul>
     <li>
        <div><b>1990:</b> First Web Browser (WWW Browser) got introduced into Market.
        <i>During that time, WWW Browser supports only static HTML Pages.</i></div>
     </li>
     <li>
        <div><b>1994:</b> First Commercial Web Browser (Netscape Navigator Browser) got
         introduced into the Market.</div>
        <div class="list-group">
           <div class="list-group-item">
            <div><b>Netscape Navigator Browser:</b></div>
            <div>It is an Graphical User Interface Browser for Non-technical People
            (But, still, it supports only Static HTML Pages).
            </div>
           </div><!--/.list-group-item -->
        </div><!--/.list-group -->
     </li>
     <li>
        <div><b>1995:</b> Brendan Eich invented LiveScript
           and Later renamed it as JavaScript and introduced this technology into 
           Netscape Navigator Browser for implementing Dynamic Content.</div>
     </li>
     <li>
        <div><b>1997:</b> European Computer Manufacturers Association (ECMA) adopted 
        Javascript and made it as ECMA Standard. <code>Therefore, Official Name of Javascript 
        became as ECMAScript.</code></div>
     </li>
    </ul>
