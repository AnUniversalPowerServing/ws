 <div><h4 class="font-red"><b>Architecture of Java</b></h4></div>
 <div>
 Java Architecture is build based on two components:
 <ol>
 <li><b>J</b>ava <b>D</b>evelopment <b>K</b>it (<b>JDK</b>)</li>
 <li><b>J</b>ava <b>R</b>untime <b>E</b>nvironment (<b>JRE</b>)</li>
 </ol>
 <div class="list-group">
 <div class="list-group-item">
 To develop Applications as a developer and run as a consumer, you need <b>JDK</b>.<br/>
 To run already developed Application only as a consumer, you need <b>JRE</b>.
 </div>
 </div>
 <b>Java Development Kit (JDK)</b><br/>
 It is a Software Development Environment that used to develop and run 
 Java Applications and Applets. It includes:
 <ol>
 <li><b>J</b>ava <b>R</b>untime <b>E</b>nvironment (<b>JRE</b>)</li>
 <li>Intrepreter/loader (java)</li>
 <li>Compiler (javac)</li>
 <li>Archiver (jar)</li>
 <li>Documentation Generator (javac)</li>
 <li>Other Tools</li>
 </ol>
 <div class="list-group mbot0">
 <div class="list-group-item">
 JDK contains Inbuild JRE that runs the developed 
 Java Applications and Applets. Without JRE, developed 
 Java Applications and APPLETS doesn't executes.
 </div>
 </div>
 <br/>
 <b>Java Runtime Environment (JRE or Java RTE)</b><br/>
 It is a Software Execution Environment that used to run 
 Java Applications and Applets already developed.
 </div>
 
