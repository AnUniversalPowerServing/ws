<div><h4 class="font-red"><b>How Google Search Engine Works?</b></h4></div>
<div>
 <div>Google follows three steps to generate results from Websites:</div>
 <div>
    <ol>
     <li>
        <div><b>Crawling:</b></div>
        <div class="jumbotron mbot5p">
          This is the First Step for finding pages exists in the Web page.
        </div><!--/.jumbotron -->
        <div>
          Crawler works in two ways:
          <ul>
            <li>
              <div><b>Crawls as an Anonymous:</b></div>
              <div>
                When Crawler visit a Website as a User, it don't know how many pages do the website 
                will have. Since, all the URLs of the Website are not mentioned at one place in 
                a file which acts as a Central Registry. The only way to know the pages is 
                getting web links from home page. And then visiting the Web links and collecting new 
                web links under them. And finally makes out a list.
              </div>
            </li>
            <li>
              <div><b>Crawls the SiteMap (which acts as Central Registry):</b></div>
              <div>
                When a Website contains <code>sitemap.xml</code> (which contains list of all 
                pages) under root location, Googlebot crawls it.
              </div>
            </li>
          </ul>
        </div>  
        <div>
        Once Googlebot discovers a Page URL, it find out what's on it.   
        </div>
     </li>
    </ol>
 </div>
